    public void fillLOList(LOList loList){
        
        this.loList = loList;
        
        jlNumLOResult.setText(String.valueOf(loList.getLOList().size()));
        
        for(LOM lom : loList.getLOList()){
            jcbLOList.addItem(lom.getLoId() + " - " + lom.getTitle());
        }
        
    }
    
    public void fillTreeLO(LOM lom){
        

        DefaultMutableTreeNode lo = new DefaultMutableTreeNode(
                lom.getLoId()+ " - " + lom.getTitle());
        
        DefaultMutableTreeNode accesibilityRecommendationTreeElement = new DefaultMutableTreeNode(
                    "Accesibilidad: " + Float.valueOf(lom.getAccessibilityScore()) / 10 * 100 + " %");
        
        lo.add(accesibilityRecommendationTreeElement);
        
        //Agregar las recomendaciones hechas por el agente
        for(String recommendation : lom.getRecommendationList()){
            DefaultMutableTreeNode recommendationTreeElement = new DefaultMutableTreeNode(
                    recommendation);
            
            lo.add(recommendationTreeElement);
        }
        
        loTreeList.add(lo);
        
        model.reload();
    }

