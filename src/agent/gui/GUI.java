    private void jbSearchLOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSearchLOActionPerformed
        
        jcbLOList.removeAllItems();
        
        Gson gson = new Gson();

        SearchLO searchLO = new SearchLO();

        searchLO.setSearchString(jtfSearchString.getText());
        
        if(!jtfLOId.getText().trim().equals("")){
            searchLO.setSearchLOId(Integer.parseInt(jtfLOId.getText()));
        }

        String json = gson.toJson(searchLO);

        Utils utils = new Utils();
        utils.sendMessage(interfaceAgent,
            new AID("ControllerAgent", AID.ISLOCALNAME),
            "obj;" + json + ";" + "msg;" + "search lo",
            ACLMessage.INFORM);
    }//GEN-LAST:event_jbSearchLOActionPerformed

